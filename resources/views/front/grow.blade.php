<h2>Building resources and knowledge.</h2><br> 
With water security in place, communities start an organic farmer training and skills transfer programme at our Farmer Training Centre where they address vital issues such as water conservation, soil enrichment, erosion prevention, nutrition, diversified crop management and symbiotic planting. We also provide hands-on training at demonstration plots within the community, which they dig and farm collectively. These local plots teach farmers how to effectively feed a family of 8 and have been key to expanding and re-iterating the training to more community members. Peer to Peer training is encouraged and our Haller community trainers attend the plots regularly in the early stages to provide additional technical support and advice. <br> <br>

 

Training is “climate-smart,” designed for the needs of sub-Saharan farmers to work with terrain that has rendered food production difficult. It encompasses water conservation, soil improvement, erosion control and terracing and then followed by horticulture and the importance of good nutrition.
<br> <br>
 

Once communities have successfully adopted these basic techniques independently we introduce advanced farmer training methods such as aquaculture, forestry, bee-keeping and livestock rearing aimed at improving household income and enabling communities to earn an income. Training in the importance of recording income and expenditure and basic business skills is also included.
<br> <br>


Following this stage, nutrition improves, food production is maximised and the landscape is greened with a more diverse range of crops and trees planted. Farmers have their own family plots, in addition to the collective community plots while neighbouring communities, smallholders and self-help groups often begin to replicate these techniques. <br><br>

