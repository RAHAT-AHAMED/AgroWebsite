<h2>Helping communities grow</h2> <br> 
As more farmers make the Haller Journey, more local communities emerge and we make sure the infrastructure is there to help them grow. Guided by Dr Haller’s innovative thinking, we improve health, expand education and equip people with the knowhow to start and run businesses that power economies while protecting the environment. <br> <br>

Farmers need to be “fit and able” to farm and targeting preventable illness is key to our Healthcare programme. This service is particularly important to the women and children, who make up  approximately 80% of these rural communities. In conjunction with the ministry of agriculture, we provide access to the Haller health clinic, an under 5’s vaccination programme, ante and post-natal care, education workshops on nutrition, hygiene, disease prevention and access to our recently opened and much-needed family planning clinic. We also operate a weekly community outreach service to reach the more remote communities. <br> <br>

 

The clinic is staffed by a clinician, nurse and a diagnostic lab technician. On average, we conduct 7,000 patient consultations, deliver 3,500 vaccinations for children under 5 and around 1,500 mobile appointments annually. Access to affordable healthcare and family planning ensure healthier, thriving communities with less malnourished children and a reduction in preventable disease. Women can now plan their desired family size, thereby further improving future livelihoods. Haller believes in a holistic approach where a community can only thrive when it is healthy. <br> <br>

Education underpins all we do. In addition to the adult focused farmer training we run an Education Centre for children – The Nguuni Education Centre which is next to Haller’s clinic. The education centre is open daily, and houses a children’s library, an IT centre and an outdoor auditorium where we run a variety of environmental activities and outdoor screenings of educational films for both children and adults. <br> <br>

All of Haller’s programmes can be used to generate an income, which shows farmers the tangible rewards of using sustainable techniques. Fish, food, biogas and solar energy can all be sold. <br> <br>

 

Haller’s nano-enterprise programme helps register communities as self-help groups so that they can set up savings accounts. We call it nano-enterprise because the people we help are so far below the poverty threshold, they do not qualify for micro-finance loans. Haller provides seed capital to help community members start small businesses and teach them skills such as bookkeeping to help them manage their money effectively. These nano-enterprise initiatives help people move beyond subsistence farming so that they can start to build sustainable lives for themselves. <br> <br>

 

 











